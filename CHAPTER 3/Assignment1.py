# write a program that takes a senteence and prints
#1. Total Characters (len())
#2. Uppercase version
#3. Lowercase version



message = input("Enter a sentence: ") # message -"Clouds drift slowly, carrying dreams across endless skies of wonder."
Total_Characters = len(message)
print("Total characteers::",Total_Characters)  # total characters

Uppercase_version = message.upper()
print("Uppercase version::"+Uppercase_version)  # uppercase version

Lowercase_version = message.lower()
print("Lowercase version::"+Lowercase_version)  # lowercase version